def soma(a, b):
    return a + b

def subtracao(a, b):
    return a - b

def multiplicacao(a, b):
    return a * b

def divisao(a, b):
    if b != 0:
        return a / b
    else:
        return "Erro: divisão por zero"

def potenciacao(a, b):
    return a ** b

def divisao_inteira(a, b):
    if b != 0:
        return a // b
    else:
        return "Erro: divisão por zero"

def modulo(a, b):
    if b != 0:
        return a % b
    else:
        return "Erro: divisão por zero"

