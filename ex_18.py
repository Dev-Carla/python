i1 = int(input("Digite o 1º número inteiro: "))
i2 = int(input("Digite o 2º número inteiro: "))
r = float(input("Digite o número real: "))

produto = (2 * i1) * (i2 / 2)
soma = (3 * i1) + r
cubo = r ** 3

print("Produto do dobro do 1º com metade do 2º:", produto)
print("Soma do triplo do 1º com o real:", soma)
print("Real ao cubo:", cubo)
